THSprite = {
	hp=1,
	power=1,
	grazed=false
	}

function THSprite.new(obj)
	obj = extend(THSprite, obj or {})
	return Sprite.new(obj)
end

function THSprite:onCollision(other, myNode, otherNode)
	self.hp = self.hp - other.power
	if self.hp <= 0 then
		if self:destroy() then
			self:dropItems()
			Explosion.new(self)
		end
	end
end

function THSprite:dropItems()	
end

